package net.wagsn.note.memo;

import android.location.Location;

/**
 * 地点
 * Created by Wagsn on 2019/7/6.
 */
public class Site {

    /**
     * 标识
     */
    public String id;

    /**
     * 名称（如，重庆市渝北区）
     */
    public String name;

    /**
     * 定位（Android 定位）
     */
    public Location location;

    /**
     * 市
     */
    public String city;

    /**
     * 区
     */
    public String district;

    /**
     * 县
     */
    public String county;

    /**
     * 镇
     */
    public String town;

    /**
     * 村
     */
    public String village;

    /**
     * 街道
     */
    public String street;

    /**
     * 路
     */
    public String road;

    /**
     * 组
     */
    public String group;

    /**
     * 号
     */
    public String no;
}
